package com.charter.clientrewards.pojo;

public class RewardsSummary {
	
	int id;
	String name;
	int firstMonthRewardSummary;
	int secondMonthRewardSummary;
	int thirdMonthRewardSummary;
	int totalRewardSummary;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getFirstMonthRewardSummary() {
		return firstMonthRewardSummary;
	}
	public void setFirstMonthRewardSummary(int firstMonthRewardSummary) {
		this.firstMonthRewardSummary = firstMonthRewardSummary;
	}
	public int getSecondMonthRewardSummary() {
		return secondMonthRewardSummary;
	}
	public void setSecondMonthRewardSummary(int secondMonthRewardSummary) {
		this.secondMonthRewardSummary = secondMonthRewardSummary;
	}
	public int getThirdMonthRewardSummary() {
		return thirdMonthRewardSummary;
	}
	public void setThirdMonthRewardSummary(int thirdMonthRewardSummary) {
		this.thirdMonthRewardSummary = thirdMonthRewardSummary;
	}
	public int getTotalRewardSummary() {
		return totalRewardSummary;
	}
	public void setTotalRewardSummary(int totalRewardSummary) {
		this.totalRewardSummary = totalRewardSummary;
	}


}
